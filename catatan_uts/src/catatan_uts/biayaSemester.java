    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String jurusan = jComboBox1.getSelectedItem().toString();
        int teori     = Integer.parseInt(jTextField1.getText());
        int praktik     = Integer.parseInt(jTextField2.getText());
        
        double sks_teori = teori * 125000;
        double sks_praktik = praktik * 175000;
        double total    = spp(jurusan) + sks_teori + sks_praktik;
        
        jLabel4.setText(jurusan);
        jLabel5.setText(String.valueOf(sks_teori));
        jLabel6.setText(String.valueOf(sks_praktik));
        jLabel7.setText(String.valueOf(spp(jurusan)));
        jLabel8.setText(String.valueOf(total));
    }//GEN-LAST:event_jButton1ActionPerformed

