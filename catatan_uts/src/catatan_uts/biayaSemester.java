    private int spp(String jurusan) {
        if(jurusan.equals("TI")) {
            return 500000;
        } else if(jurusan.equals("MI")) {
            return 500000;
        } else if(jurusan.equals("TK")) {
            return 500000;
        } else if(jurusan.equals("SI")) {
            return 600000;
        } else if(jurusan.equals("TA")) {
            return 600000;
        }
        return 0;
    }
